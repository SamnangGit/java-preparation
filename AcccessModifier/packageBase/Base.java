package packageBase;

public class Base {
    // public
    public int addition(int a, int b) {
        return a + b;
    }

    // private
    private int substraction(int a, int b) {
        return a - b;
    }

    // protected
    protected int multiplycation(int a, int b) {
        return a * b;
    }

    // default
    int division(int a, int b) {
        return a / b;
    }
}
